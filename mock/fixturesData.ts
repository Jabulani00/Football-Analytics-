export function getLeagueIdFromMatchId(matchId: string): string {
  const prefix = matchId.split('_')[0];
  return prefix in mockFixtures ? prefix : 'spl';
}

export function getFixturesForLeague(leagueId: string): Fixture[] {
  return mockFixtures[leagueId] ?? [];
}

export function getFixtureById(matchId: string): Fixture | undefined {
  for (const fixtures of Object.values(mockFixtures)) {
    const found = fixtures.find((f) => f.id === matchId);
    if (found) return found;
  }
  return undefined;
}

export function countLiveFixtures(leagueId: string, date = '2026-05-21'): number {
  return getFixturesForLeague(leagueId).filter(
    (f) => f.date === date && f.status === 'LIVE',
  ).length;
}

export function countFixturesOnDate(leagueId: string, date = '2026-05-21'): number {
  return getFixturesForLeague(leagueId).filter((f) => f.date === date).length;
}
